package tools;


public class AdvCalcPro
{
    public int mul(int n1, int n2)
    {
        return n1 * n2;
    }
    public int div(int n1, int n2)
    {
        return n1 / n2;
    }
    public int mul(int n1, int n2, int n3)
    {
        return n1 * n2 * n3;
    }
       public double power(int n1, int n2)
    {
        return Math.pow(n1, n2);
    }
}
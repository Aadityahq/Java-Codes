public class do_while {

    public static void main(String[] args) {
        int i = 1;

        do
        {
            System.out.println("God helps people");
            i++;

            int j = 1;
            do{
                
                System.out.println("God is great " + j);
                j++;
            }while(j<5);
            
        }while(i<3);
    }
    
}
